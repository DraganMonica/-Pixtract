def extract_attributes(image_bytes: bytes, categorie: str) -> dict:
    image_base64 = prepare_image(image_bytes)
    raw = run_qwen(image_base64, categorie)

    if not raw:
        return {"error": "JSON parse failed"}

    return normalize_output(raw, categorie)

