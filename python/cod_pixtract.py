# IMAGINE
MAX_SIZE = (1024, 1024)

def prepare_image(image_bytes: bytes) -> str:
    img = Image.open(io.BytesIO(image_bytes)).convert("RGB")
    # Nu crop, nu stretch. Daca imaginea e 4000x3000, o face 1024x768 pastrand proportia; quality= 85 e echilibrul bun intre dimensiune si claritate pentru model
    img.thumbnail(MAX_SIZE, Image.LANCZOS)  # resize proportional, fara crop
    buffer = io.BytesIO()
    img.save(buffer, format="JPEG", quality=85)
    return base64.b64encode(buffer.getvalue()).decode("utf-8")


# PROMPT
#construieste textul final care merge la model: regulile categoriei + culorile permise + nuantele; asta e ce vede modelul ca "user message".
def build_prompt(categorie: str) -> str:
    rule = RULES_MAP.get(categorie)
    if not rule:
        raise ValueError(f"Categorie necunoscuta: {categorie}")

    schema = SCHEMA[categorie]
    culori = schema["Culoare de baza"]
    nuante = {k: v for k, v in NUANTE.items()}

    return (
        f"{rule}\n\n"
        f"Allowed colors: {culori}\n"
        f"Shades per color: {nuante}\n\n"
        f"Return ONLY a valid JSON object. No extra text."
    )


# PARSARE
def parse_json(text: str) -> dict:
    try:
        return json.loads(text)
    except:
        match = re.search(r"\{.*\}", text, re.DOTALL)
        if match:
            try:
                return json.loads(match.group())
            except:
                pass
    return None


# NORMALIZARE
def normalize_color(data: dict) -> dict:
    baza = data.get("Culoare de baza")
    nuanta = data.get("Nuanta")

    if not baza or baza not in NUANTE:
        data["Nuanta"] = None
        return data

    if not nuanta or nuanta not in NUANTE[baza]:
        data["Nuanta"] = None

    return data

# curata ce returneaza modelul
# 3 lucruri face: scoate ':' de la sfarsitul cheilor, daca returneaza o lista ia primul element si aplica logica de nuanta
def normalize_output(data: dict, categorie: str) -> dict:
    if not data:
        return None

    # curata chei cu ':' la final- e posibil sa puna el un ':'
    data = {k.strip().rstrip(":").strip(): v for k, v in data.items()}

    # daca valoare e lista, ia primul element
    data = {
        k: (v[0] if isinstance(v, list) and v else v)
        for k, v in data.items()
    }

    data = normalize_color(data)

    if "Poveste/Personaj" in data:
        if data.get("Imprimeu") != "Desene animate":
            data["Poveste/Personaj"] = None

    return data


